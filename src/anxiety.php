    <p>Anxiety disorders are typically diagnosed and treated by a team of psychiatrists and psycho-therapists or counselors. The treatment generally is a combination of medication (like anti-anxiety and anti-depressants) and therapy (like cognitive behavioral therapy and mindfulness and relaxation).</p>
