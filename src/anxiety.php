  <div class="item3">
    <p id="heading">Types of Anxiety Disorders</p>
    <div class="disod">
      <p id="head">1. Generalized Anxiety Disorder</p>
      <p>
        Generalized anxiety disorder is a chronic mental disorder, characterized by long lasting anxiety, about nothing in particular. It usually lasts for 6 months or more. The anxiety is excessive, or an overreaction, to the situation at hand. They usually worry about health, work, loved ones etc. People with Generalized Anxiety Disorder have trouble in identifying what exactly is causing them anxiety and how to deal with these thoughts. Generalized Anxiety disorder eats into the ability to perform everyday activities like work and activities at home, as it is a constant, nagging anxiety that something will go wrong.
      </p>
      <p id="head">2. Social Phobia</p>
      <p>
        Social phobia is an excessive, irrational fear of social interactions. The interactions can be as common as interactions at the work place or eating out at a restaurant. The main phobia is about being embarrassed and judged in front of other people. The persons are excessively self-conscious, even when they need not be. It often goes to the extent of the person avoiding human contact, such that leading a normal life becomes very difficult. People with social phobia often fear even the anticipation of a social interaction, and again, may go to great lengths to avoid such interactions.      </p>
      <p id="head">3. Obsessive Compulsive Disorder</p>
      <p>
        Obsessive compulsive disorder is a common, chronic disorder where the person has repetitive, intrusive thoughts (obsessions) that lead them to perform certain actions (compulsions) repeatedly, believing that they can avoid harm if they perform these actions. OCD usually lasts a long period, but can be controlled effectively with a combination of medication and therapy. The obsessions are often irrational, and even the person suffering knows that his/her fears are irrational, but performing such compulsions are beyond their control. Repeatedly washing hands or doing the same thing a specific number of times are very common symptoms of OCD.      </p>
      <p id="head">4. Post-Traumatic Stress Disorder</p>
      <p>
        Post-traumatic stress disorder is a chronic or short term mental disorder characterized by a sense of anguish, anxiety and stress that lingers on after experiencing a traumatic, shocking or dangerous event. For instance, it is stress caused due to the aftermath of a violent accident or bloody fight. Most often it is characterized by flashbacks to the traumatic event, and could even manifest in nightmares and phobias related to the event. The sense of fear continues even after they are no longer exposed to the danger. Sometimes PTSD could develop years after the occurrence of the event.      </p>
      <p id="head">5. Panic Disorder & Panic Attack</p>
      <p>
        Panic means a feeling of anxiety. Panic in small quantities before or after a significant event is natural, but feeling anxious and panicking for no apparent reason, or a very insignificant reason, is characterized as panic disorder. During a panic attack, the reaction to the stimulus is out of proportion and exaggerated. Panic attacks are physical manifestations of the anxiety a person is experiencing, usually (but not only) like shortness of breath, tightness in the chest area or dizziness. The exact cause of panic disorders is unknown, but they may be a combination of genetic and environmental factors.      </p> 
    </div>
