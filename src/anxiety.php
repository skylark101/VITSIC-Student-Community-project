    <p id="heading">What is Anxiety?</p>
    <p>
      Anxiety is an emotion. It is an emotion of dread, fear and worry that something will go wrong. While anxiety in small doses is good for us, because it motivates us to do our best, when anxiety gets out of hand, it impairs us from leading a happy, peaceful life and is then called an anxiety disorder.
     <br><br> Anxiety tends to be the result of feelings of inadequacy, indecision and uncertainty, being ill prepared, procrastination, etc.
      <br><br> In order to understand what anxiety is, we must understand the ‘flight or fight’ mechanism. Our psyche tends to use certain mechanisms as a way to escape or dissolve anxiety. These mechanisms could have three characteristics – fight, flight or split. They are called defense mechanisms. In people who suffer from anxiety, the defense mechanism of fight or flight, is a little exaggerated. They tend to have an over-reaction to a stimulus, basically a reaction not in proportion with the perceived threat.
    </p>
