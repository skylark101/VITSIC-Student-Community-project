  <div class="item2">
    <p id="heading">What is Anxiety Disorder?</p>
    <p>When a person experiences heightened feelings (fear, worry, suppressed rage etc) along with increased physical activity, (palpitations, restlessness, sweating etc) it is called anxiety. When one does not understand, regulate and equip oneself to deal with the underlying causes of anxiety, it can become a handicap, the handicap becoming an anxiety disorder. Symptoms counted as anxiety include –</p> <br>
    <ul>
      <li>Inability to sleep or sudden waking up in the night</li>
      <li>Constantly feeling worried, tensed</li>
      <li>Avoiding everyday situations or feeling irritable while managing everyday living</li>
      <li>Restlessness</li>
      <li>Feeling numb or blanking out mentally</li>
      <li>Anticipating the worst.</li>
      <li>Does not equip one for fight or flight</li>
      <li>Freezes the person’s ability to think and act</li>
      <li>Restrict choices and act detrimental to problem-solving, decision-making and control over one's mental processes - attention, memory, relationships, judgment, ability to respond with maturity, etc.</li>
    </ul>
    <p>
      Also anxiety can become a disorder when: - <br>
      Defense mechanisms are used excessively – there is a risk of a person escaping or misinterpreting reality in the use of defense mechanisms. If an individual uses defenses excessively then that individual is running the risk of contorting reality and living a dual existence.
    </p>
