  <div class="item2">
    <p id="heading">Symptoms of Depression</p>
    <p>Depression symptoms can vary from mild to severe. According to DSM- V, a person may be called to suffer from depression if s/he has 5 out of 9 symptoms nearly everyday.</p> <br>
    <ol type="1">
      <li>Depressed mood or irritable most of the day, nearly every day, as indicated by either subjective report (e.g., feels sad or empty) or observation made by others (e.g., appears tearful).</li>
      <li>Decreased interest or pleasure in most activities, most of each day.</li>
      <li>Significant weight change (5%) or change in appetite</li>
      <li>Change in sleep: Insomnia or hypersomnia</li>
      <li>Change in activity: Psychomotor agitation or retardation</li>
      <li>Fatigue or loss of energy.</li>
      <li>Guilt/worthlessness: Feelings of worthlessness or excessive or inappropriate guilt</li>
      <li>Concentration: diminished ability to think or concentrate, or more indecisiveness</li>
      <li>Suicidality: Thoughts of death or suicide, or has suicide plan</li>
    </ol>
  </div>

  <div class="item3">
    <p id="heading">Types of Depressive Disorders</p>
    <div class="disod">
      <p id="head">1. Major or Unipolar Depression</p>
      <p>
        As the name suggests, feeling depressed for most of the time is called major depressive disorder.
      </p>
      <p id="head">2. Dysthmia or Persistent Depressive Disorder (PDD)</p>
      <p>
        Depression that persists for more than 2 years is called persistent depressive disorder.      </p>
      <p id="head">3. Seasonal Affective Disorder (SAD)</p>
      <p>
        Seasonal affective depression generally happens during winter due to lack of exposure to sunlight.      </p>
      <p id="head">4. Psychotic Depression</p>
      <p>
        Psychotic depression is a kind of major depression, coupled with hallucinations, paranoia and delusions.      </p>
      <p id="head">5. Postpartum Depression (PPD)</p>
      <p>
        Depression that arises right after the birth of a child in mothers is called postpartum depression.      </p> 
    </div>
  </div>

  <div class="item4">
    <p id="heading">Depression Treatment</p>
    <p>Depression is usually treated with a combination of medication and therapy. There is no standard treatment method for depression, as it varies from person to person. Medication could be mood stabilizers and antidepressants while therapy is usually Cognitive Behavioral therapy.

      Antidepressants usually consist of one of these 3 chemicals, serotonin, dopamine and norepinephrine.
      
      Cognitive behavioral therapy is further divided into cognitive therapy and exposure therapy.
      
      A balanced diet and regular activity and exercise can also be part of the treatment plan.</p>
