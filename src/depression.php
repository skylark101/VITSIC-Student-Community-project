    <p id="heading">What is Depression?</p>
    <p>
      Depression (major depressive disorder) is a common medical illness that negatively affects how you feel, the way you think and how you act. Depression causes feelings of sadness, fatigue, lack of concentration and/or a loss of interest in activities once enjoyed. It can lead to a variety of emotional and physical problems and can decrease a personâ€™s ability to function at work and at home. Fortunately, it is treatable.     <br><br> Anxiety tends to be the result of feelings of inadequacy, indecision and uncertainty, being ill prepared, procrastination, etc.
      <br><br> Depression can become a serious illness and would need a clinical intervention. Depression is caused by the changes in the brain chemistry, but it is not restricted to that. Research has pointed out that there are various other factors that may contribute to depression which includes genetics, changes in hormones, certain medical conditions, an ongoing stress, anxiety and grief, or other difficulty in life. These factors alone or in combination can change the brain chemistry and lead to depression.
